    public User registerNewUser(User user) {
        Role role = roleDao.findById("User").get();
        Set<Role> userRoles = new HashSet<>();
        userRoles.add(role);
        user.setRole(userRoles);
        user.setUserPassword(getEncodedPassword(user.getUserPassword()));
        user.setActive(false);
        user.setTokenExp(false);
        String token = UUID.randomUUID().toString();
        user.setVerificationCode(token);

        String link = "http://localhost:4200/verification/"+token;

        String email = buildEmail(user.getUserName(),link);
        emailService.send(user.getUserName(),email);
        return userDao.save(user);
    }

    public VerifyEmailResponse verifyEmail(String token){
        User user= userDao.findUserByVerificationCode(token);
        if(user.isTokenExp()){
            return  new VerifyEmailResponse("Email already verified");
        }
        user.setActive(true);
        user.setTokenExp(true);
        userDao.save(user);


        return new VerifyEmailResponse("Email Verified");

    }

